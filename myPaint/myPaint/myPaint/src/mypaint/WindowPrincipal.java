    private void panelDrawMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelDrawMousePressed
          
        
        if( btnLine.isSelected() ) {
            form = new Line();
        } else if( btnRectangle.isSelected() ) {
            form = new Rectangle();
        } else if ( btnElipse.isSelected() ) {
            form = new Elipse();
        } else if ( btnPen.isSelected() ) {
            form = new Pen();
            form.setX1(evt.getX());
            form.setY1(evt.getY());
            form.setOutline(panelOutline.getBackground());
            panelDraw.addForm(form); 
            panelDraw.repaint();+
            return; 
        } else if ( btnEraser.isSelected() ) {
            form = new Eraser();
            form.setX1(evt.getX());
            form.setY1(evt.getY());
            panelDraw.addForm(form);
            panelDraw.repaint();
            return; 
        }

        form.setX1( evt.getX() );
        form.setY1( evt.getY() );
        form.setOutline( panelOutline.getBackground() );
        form.setFill( panelFill.getBackground() );
        
    }//GEN-LAST:event_panelDrawMousePressed

    private void panelDrawMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelDrawMouseReleased
        
       if ( btnPen.isSelected() || btnEraser.isSelected() ) {
            return;
        }
    
        form.setX2( evt.getX() );
        form.setY2( evt.getY() );

        panelDraw.addForm( form );
        panelDraw.setFormTemp( null );

        panelDraw.repaint();
    }//GEN-LAST:event_panelDrawMouseReleased

    private void panelDrawMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panelDrawMouseDragged
        
       if ( btnPen.isSelected() ) {
        Form newPen = new Pen();
        newPen.setX1(evt.getX());
        newPen.setY1(evt.getY());
        newPen.setOutline(panelOutline.getBackground());
        panelDraw.addForm(newPen);
        panelDraw.repaint();
        
    } else if ( btnEraser.isSelected() ) {
        Form newEraser = new Eraser();
        newEraser.setX1(evt.getX());
        newEraser.setY1(evt.getY());
        panelDraw.addForm(newEraser);
        panelDraw.repaint();
        
    } else {
        form.setX2( evt.getX() );
        form.setY2( evt.getY() );
        
        panelDraw.setFormTemp( form );
        panelDraw.repaint();
    }
        
    }//GEN-LAST:event_panelDrawMouseDragged

