    private void btnUndoClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUndoClicked
        
        if ( !panelDraw.getForms().isEmpty() ) {
            List<Form> currentForms = panelDraw.getForms();
            Form lastForm = currentForms.remove(currentForms.size() - 1);
            redo.push(lastForm);

            panelDraw.setForms( currentForms );
            panelDraw.repaint();
        }
    }//GEN-LAST:event_btnUndoClicked

    private void btnRedoClicked(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRedoClicked
        
        if ( !redo.isEmpty() ) {
            Form formToRestore = redo.pop();
            List<Form> currentForms = panelDraw.getForms();
            currentForms.add(formToRestore);

            panelDraw.setForms( currentForms );
            panelDraw.repaint();
        }
    }//GEN-LAST:event_btnRedoClicked

    
